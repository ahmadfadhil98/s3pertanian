                Number.prototype.comma_formatter = function() {
                    return this.toString().replace(/\B(?<!\.\d*)(?=(\d{3})+(?!\d))/g, ",");
                }

let chartData = function(){
    return {
        date: 'all',
        options: [
            {
                label: 'Pilih Parameter',
                value: 'all',
            },
            {
                label: 'Aktif',
                value: 'aktif',
            },
            {
                label: 'Beasiswa',
                value: 'beasiswa',
            },
        ],
        showDropdown: false,
        selectedOption: 0,
        selectOption: function(index){
            this.selectedOption = index;
            this.date = this.options[index].value;
            this.renderChart();
        },
        data: null,
        fetch: function(){
            fetch('http://s3pertanian.test/json')
                .then(res => res.json())
                .then(res => {
                    this.data = res.dates;
                    this.renderChart();
                })
        },
        renderChart: function(){
            let c = false;

            Chart.helpers.each(Chart.instances, function(instance) {
                if (instance.chart.canvas.id == 'chart') {
                    c = instance;
                }
            });

            if(c) {
                c.destroy();
            }

            let ctx = document.getElementById('chart').getContext('2d');

            let chart = new Chart(ctx, {
                type: "line",
                data: {
                    labels: this.data.label,
                    datasets: [
                        {
                            label: "Masuk",
                            backgroundColor: "rgba(102, 126, 234, 0.25)",
                            borderColor: "rgba(102, 126, 234, 1)",
                            pointBackgroundColor: "rgba(102, 126, 234, 1)",
                            data: this.data[this.date].data.masuk,
                        },
                        {
                            label: "Wisuda",
                            backgroundColor: "rgba(237, 100, 166, 0.25)",
                            borderColor: "rgba(237, 100, 166, 1)",
                            pointBackgroundColor: "rgba(237, 100, 166, 1)",
                            data: this.data[this.date].data.wisuda,
                        },
                        {
                            label: "Aktif",
                            backgroundColor: "rgb(100, 237, 134,0.25)",
                            borderColor: "rgb(100, 237, 134,1)",
                            pointBackgroundColor: "rgb(100, 237, 134,1)",
                            data: this.data[this.date].data.aktif,
                        },
                    ],
                },
                layout: {
                    padding: {
                        right: 10
                    }
                },
                options: {
                    scales: {
                        yAxes: [{
                            gridLines: {
                                display: false
                            },
                            ticks: {
                                callback: function(value,index,array) {
                                    return value > 1000 ? ((value < 1000000) ? value/1000 + 'K' : value/1000000 + 'M') : value;
                                }
                            }
                        }]
                    }
                }
            });
        }
    }
}

            </script>
